/* 번호 이름 점수
 *  01 홍길동 90
 */
//탭키(t) 8칸 띄우기
public class Exam_06 {

	public static void main(String[] args) {
		System.out.print("번호\t이름\t점수\n");
		System.out.print("01\t");
		System.out.print("홍길동\t");
		System.out.print("90");
		System.out.print(90+"\n");
		System.out.printf("%d\n",10);//형식지정 출력(C언어)
		System.out.printf("8진수=%o\n",10);//형식지정 출력(C언어)
		System.out.printf("16진수=%x\n",10);//형식지정 출력(C언어)
	}

}
