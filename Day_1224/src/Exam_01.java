/*
 * 괄호를 열고 닫으면 메소드,함수가 됨
 */
public class Exam_01 {
	public static void main(String[] args) {
		int a; // 기억공간의 크기 1byte
		char c; // 문자,정수 2byte
		float f;// 4byte
		double d; // 8byte
		
		//V = e; <- 대입연산자 V는 변수만 가능,e는 상수
		//"" <-문자열
		// '' <-ㅁ문자
		a = 100;
		c = 'A'; // '1'
		f = 3.14f; //자동적으로 double형
		d = 3.14; // 맨뒤에 f붙이면 float형
	}

}
