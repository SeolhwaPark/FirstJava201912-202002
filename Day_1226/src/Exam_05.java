/*
 *int로 10진수값으로 출력하기
 */
public class Exam_05 {
	public static void main(String[] args) {
		int digit = 10;
		int oct = 010; //8진수
		int hex = 0x10; // 16진수
		int bin = 0b10; // 2진수
		
		System.out.println("10 진수 : " + digit);
		System.out.println("8 진수 : " + oct);
		System.out.println("16 진수 : " + hex);
		System.out.println("2 진수 : " + bin);
	}

}
